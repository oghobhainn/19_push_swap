#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	t_list	*tmp;

	if (alst && new && (*alst != new))
	{
		if (*alst == NULL)
			*alst = new;
		else
		{
			tmp = ft_lstlast(*alst);
			tmp->next = new;
		}
	}
}
